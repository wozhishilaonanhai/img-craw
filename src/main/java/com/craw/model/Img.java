package com.craw.model;

public class Img {
    private String img;
    private String imgId;

    public Img(String img, String imgId) {
        this.img = img;
        this.imgId = imgId;
    }

    public String getImg() {
        return img;
    }

    public String getImgId() {
        return imgId;
    }
}
